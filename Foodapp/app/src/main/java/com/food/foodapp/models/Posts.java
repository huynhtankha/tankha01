package com.food.foodapp.models;

public class Posts {
    int id;
    String ten_baiviet;
    String hinhanh;
    String noidung;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen_baiviet() {
        return ten_baiviet;
    }

    public void setTen_baiviet(String ten_baiviet) {
        this.ten_baiviet = ten_baiviet;
    }

    public String getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(String hinhanh) {
        this.hinhanh = hinhanh;
    }

    public String getNoidung() {
        return noidung;
    }

    public void setNoidung(String noidung) {
        this.noidung = noidung;
    }
}
